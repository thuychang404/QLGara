    public ReceiptDetailFrm(Receipt r) {
        this.receipt = r;
        initComponents();
        
        jLabelReceiptID.setText("Mã HĐ: " + Integer.toString(receipt.getId()));        
//        jLabelCarAutomaker.setText("Hãng xe: " + receipt.getCar().getAutomaker());
//        jLabelCarLicense.setText("Biển số: " + receipt.getCar().getLicensePlate());
//        jLabelCarType.setText("Dòng xe: " + receipt.getCar().getType());
        jLabelCusAddress.setText("Địa chỉ: " + receipt.getCustomer().getAddress());
        jLabelCusName.setText("Họ tên: " + receipt.getCustomer().getName());
        jLabelCusTel.setText("Số điện thoại:" + receipt.getCustomer().getTel());
        jLabelEmEmail.setText("Email: " + receipt.getCreator().getEmail());
        jLabelEmName.setText("Họ tên: " + receipt.getCreator().getFullname());
        jLabelEmTel.setText("Số điện thoại: " + receipt.getCreator().getTel());
        
        double totalPrice = 0;
        for (RepairedCar rc : r.getRepairedCars()) {
            totalPrice += rc.getUsedServices().stream().mapToDouble(UsedService::getPrice).sum() + rc.getUsedSupplies().stream().mapToDouble(UsedSupply::getPrice).sum();
        }


        DecimalFormat decimalFormat = new DecimalFormat("#,###");
        
        String price = decimalFormat.format(totalPrice);
        jLabelTotalMoney.setText("Tổng tiền: " + price);
        
        if(receipt.getPaymentType().equals("cash")) {
            jLabelCashMoney.setText("Khách tiền mặt: " + price);
            jLabelTransferMoney.setText("Khách chuyển khoản: " + "0");
        } else {
            jLabelCashMoney.setText("Khách tiền mặt: " + "0");
            jLabelTransferMoney.setText("Khách chuyển khoản: " + price);
        }
        
        DefaultTableModel model = (DefaultTableModel) this.jTableListCar.getModel();
        model.setRowCount(0);
        
        for (RepairedCar rc : receipt.getRepairedCars()) {
            model.addRow(new Object[]{
                rc.getCar().getLicensePlate(),
                rc.getCar().getType(),
                rc.getCar().getAutomaker()
            });  
            loadReceipt(rc);
        }

    }
    
    void loadReceipt(RepairedCar rc) {
        if (!rc.getUsedSupplies().isEmpty()) {
            loadUsedSupply(rc);
        } else {
            jLabelListSupply.setText("Không có linh kiện");
        }
        
        if (!rc.getUsedServices().isEmpty()) {
            loadUsedService(rc);
        } else {
            jLabelListService.setText("Không có dịch vụ");
        }

    }
    
    void loadUsedSupply(RepairedCar rc) {
        DefaultTableModel model = (DefaultTableModel) this.jTableUsedSupply.getModel();
        model.setRowCount(0);
        for (UsedSupply uss : rc.getUsedSupplies()) {
            model.addRow(new Object[]{
                uss.getId(),
                uss.getSupply().getName(),
                uss.getSupply().getPrice(),
                uss.getAmount(),
                uss.getSupply().getUnit(),
                uss.getPrice()
            });
        }
    }
    
    void loadUsedService(RepairedCar rc) {
        DefaultTableModel model = (DefaultTableModel) this.jTableUsedService.getModel();
        model.setRowCount(0);
        for (UsedService us : rc.getUsedServices()) {
            model.addRow(new Object[]{
                us.getId(),
                us.getService().getName(),
                us.getService().getPrice(),
                us.getAmount(),
                us.getService().getUnit(),
                us.getPrice()
            });            
        }
    }
    

