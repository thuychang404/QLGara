    private void jButtonBackActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBackActionPerformed
        // TODO add your handling code here:
        IncomeStat incomeStat = null;
        
        // Sử dụng lớp Calendar để trích xuất năm và tháng từ đối tượng Date
        Calendar calendar = Calendar.getInstance();
        calendar.setTime(receipt.getReceiptDate());
        int year = calendar.get(Calendar.YEAR);
        int month = calendar.get(Calendar.MONTH) + 1;
        
        incomeStat = new IncomeStat(month, year, 0);
        
        this.setVisible(false);
        ReceiptStatFrm receiptStatFrm = new ReceiptStatFrm(incomeStat);
        receiptStatFrm.setVisible(true);
        receiptStatFrm.setTitle("Car Garage");
    }//GEN-LAST:event_jButtonBackActionPerformed

